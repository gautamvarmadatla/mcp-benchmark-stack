#!/usr/bin/env bash
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT="$SCRIPT_DIR/.."
cd "$ROOT"
FETCH_URL="${FETCH_SERVER_URL:-http://127.0.0.1:8001/mcp}"
AUTH_URL="${AUTH_SERVER_URL:-http://127.0.0.1:8002/mcp}"
echo "=== MCP Conformance Check (unstable/WIP) ==="
echo ""
echo "--- Listing available scenarios ---"
npx --yes @modelcontextprotocol/conformance list 2>&1 || echo "(conformance list failed - may need npm install)"
echo ""
echo "--- Server conformance: fetch_http_server ---"
npx --yes @modelcontextprotocol/conformance server --url "$FETCH_URL" 2>&1 | tee evidence/logs/conformance_fetch.log || echo "(conformance check failed or server not running)"
echo ""
echo "--- Server conformance: auth_http_server ---"
npx --yes @modelcontextprotocol/conformance server --url "$AUTH_URL" 2>&1 | tee evidence/logs/conformance_auth.log || echo "(conformance check failed or server not running)"
echo ""
echo "Conformance logs in evidence/logs/"
